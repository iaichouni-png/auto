// Fonction pour obtenir la traduction
function getTranslation(lang, key) {
    const keys = key.split('.');
    let value = translations[lang];
    
    for (const k of keys) {
        if (value && value[k] !== undefined) {
            value = value[k];
        } else {
            return key; // Retourne la clé si la traduction n'existe pas
        }
    }
    
    return value;
}

